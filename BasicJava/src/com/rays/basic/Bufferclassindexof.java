package com.rays.basic;

public class Bufferclassindexof {
	public static void main(String[] args) {
		StringBuffer un = new StringBuffer("vijay ");
		un.append("Deenanath Chohan");
		System.out.println(un.indexOf("Ch"));
		System.out.println(un.replace(0, 5,("jay")));
		System.out.println(un.reverse());
		System.out.println(un.charAt(8));
		System.out.println(un.length());
		
	}

}
